package kr.or.bus.dto;

public class RoutePathDTO {
	private String r_x;
	private String r_y;
	private String r_order;
	private String r_num;
	
	public String getR_x() {
		return r_x;
	}
	public void setR_x(String r_x) {
		this.r_x = r_x;
	}
	public String getR_y() {
		return r_y;
	}
	public void setR_y(String r_y) {
		this.r_y = r_y;
	}
	public String getR_order() {
		return r_order;
	}
	public void setR_order(String r_order) {
		this.r_order = r_order;
	}
	public String getR_num() {
		return r_num;
	}
	public void setR_num(String r_num) {
		this.r_num = r_num;
	}
	
	
}
