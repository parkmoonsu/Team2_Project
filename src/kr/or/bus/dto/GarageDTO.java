package kr.or.bus.dto;

public class GarageDTO {
	private String g_num;
	private String g_name;
	private String g_x;
	private String g_y;
	private String g_addr;
	private String g_space;
	private String g_currentpark;
	
	public String getG_num() {
		return g_num;
	}
	public void setG_num(String g_num) {
		this.g_num = g_num;
	}
	public String getG_name() {
		return g_name;
	}
	public void setG_name(String g_name) {
		this.g_name = g_name;
	}
	public String getG_x() {
		return g_x;
	}
	public void setG_x(String g_x) {
		this.g_x = g_x;
	}
	public String getG_y() {
		return g_y;
	}
	public void setG_y(String g_y) {
		this.g_y = g_y;
	}
	public String getG_addr() {
		return g_addr;
	}
	public void setG_addr(String g_addr) {
		this.g_addr = g_addr;
	}
	public String getG_space() {
		return g_space;
	}
	public void setG_space(String g_space) {
		this.g_space = g_space;
	}
	public String getG_currentpark() {
		return g_currentpark;
	}
	public void setG_currentpark(String g_currentpark) {
		this.g_currentpark = g_currentpark;
	}
	
}
