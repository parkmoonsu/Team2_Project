package kr.or.bus.dto;

public class BusDTO {
	String b_vehiclenum;
	String r_num;

	public String getR_num() {
		return r_num;
	}

	public void setR_num(String r_num) {
		this.r_num = r_num;
	}

	public String getB_vehiclenum() {
		return b_vehiclenum;
	}

	public void setB_vehiclenum(String b_vehiclenum) {
		this.b_vehiclenum = b_vehiclenum;
	}
}
